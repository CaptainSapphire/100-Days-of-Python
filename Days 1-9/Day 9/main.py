#Casting is where we explicitly tell the computer that what we are typing is a number and not a letter.
#int and floats
print("Welcome to the \033[33mGENERATION GENERATOR! 🥳")
year = int(input("\033[37m What \033[31myear\033[37m were you born?"))

if year >= 1925 and year <= 1946:
  print("\033[37mYou are part of the... \033[31mTraditionalists 🕰️")
elif year >= 1947 and year <= 1964:
  print("\033[37mYou are part of the... \033[31mBaby Boomers 🐤💥")
elif year >= 1965 and year <= 1981:
  print("\033[37mYou are part of the... \033[31mGeneration 🇽")
elif year >= 1982 and year <= 1995:
  print("\033[37mYou are part of the... \033[31mMillenials 😂💯")
#GEN Z UPDATED based on a google search
elif year >= 1996 and year <= 2011:
  print("\033[37mYou are part of the... \033[31mGeneration Z📱📚")
elif year >= 2010 and year <= 2023:
  print("\033[37mYou are part of the... \033[31mGeneration Alpha 🚼")


